package bestiu;

public class IncrementDecrementDemo {

	public static void main(String[] args) {
		   int a=10;
		    System.out.println("++a :" + a++);
		    System.out.println("after post-increment of a" + a);
		    
		    System.out.println("++a :" + ++a);
		    
		    System.out.println("a--:" + a--);
		    System.out.println("after post-decrement of a" + a);
		    
		    System.out.println("--a" + --a);
	}

}
